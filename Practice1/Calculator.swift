import Foundation

public class Calculator {
    
    public static func add(_ target1: Int, _ target2: Int) -> Int {
        return target1 + target2
    }
    
    public static func sub(_ target1: Int, _ target2: Int) -> Int {
        return target1 - target2
    }
    
    public static func div(_ target1: Int, _ target2: Int) -> Int {
        return target1 / target2
    }
    
    public static func mul(_ target1: Int, _ target2: Int) -> Int {
        return target1 * target2
    }
    
}
